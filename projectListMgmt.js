function formatProjectList() {
    const { projects } = data;

    return projects.map(item => {
        return {
            "Name": item.Name,
            "CreatedBy": item.CreatedBy,
            "StartDate": item.StartDate,
            "Status": item.StartDate,
            "Owner": item.Owner
        }
    })
}

const prjList = formatProjectList();

const handleSearch = (val) => {
    const key = val?.toLowerCase();
    const list = [];
    const k = [];

    prjList?.forEach(item => {
        if (key && (item?.Owner?.trim().toLowerCase().search(key) >= 0 ||
            item?.Name?.trim().toLowerCase().search(key) >= 0 ||
            item?.Status?.trim().toLowerCase().search(key) >= 0)) {
            if (list.findIndex(itm => itm.Owner === item.Owner) === -1) {
                list.push(item);
            }
            k.push(item);
        }
    });

    return k;
}

